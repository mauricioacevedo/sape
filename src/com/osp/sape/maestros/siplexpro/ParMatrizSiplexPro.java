package com.osp.sape.maestros.siplexpro;


/**
 * @hibernate.class table="siplexpro_matriz" 
 * @author Andres Aristizabal
 *
 */
public class ParMatrizSiplexPro {

	private int par;
	private int telefono;
		
	public ParMatrizSiplexPro() {
		
	}
	
	public ParMatrizSiplexPro(int par, int telefono) {
		this.par = par;
		this.telefono = telefono;
	}
	
	/**
	 * @hibernate.id type = "int" generator-class = "assigned"
	 * @return
	 */
	public int getPar() {
		return par;
	}

	public void setPar(int par) {
		this.par = par;
	}

	/**
	 * @hibernate.property type = "int"
	 * @return
	 */
	public int getTelefono() {
		return telefono;
	}

	public void setTelefono(int telefono) {
		this.telefono = telefono;
	}
	
	
	
}
