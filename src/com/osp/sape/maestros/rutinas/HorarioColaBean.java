package com.osp.sape.maestros.rutinas;

import java.util.Date;
import java.util.List;

/**
 * 
 * @author Andres Aristizabal y Mauricio Acevedo
 */
public class HorarioColaBean {
    
    private String cola;
    private String titulo;
    private String usuarioCambio;
    private Date fechaCambio;
    private List listaHorarios;
    private List listaDias;
	
    public String getCola() {
        return cola;
    }
    
    public void setCola(String cola) {
        this.cola = cola;
    }
    
    public List getListaHorarios() {
        return listaHorarios;
    }
    
    public void setListaHorarios(List listaHorarios) {
        this.listaHorarios = listaHorarios;
    }
    
    
    public List getListaDias() {
        return listaDias;
    }
    
    public void setListaDias(List listaDias) {
        this.listaDias = listaDias;
    }
    
    public String getTitulo() {
        return titulo;
    }
    
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

	public Date getFechaCambio() {
		return fechaCambio;
	}

	public void setFechaCambio(Date fechaCambio) {
		this.fechaCambio = fechaCambio;
	}

	public String getUsuarioCambio() {
		return usuarioCambio;
	}

	public void setUsuarioCambio(String usuarioCambio) {
		this.usuarioCambio = usuarioCambio;
	}
    
    
}
